SCROLON(LRHYTM,LRHYBM) ;
 ;MAKES A SCROLLING REGION ON THE SCREEN
 ;LRHYTM=TOP MARGIN(Y), LRHYBM=BOTTOM MARGIN(Y)
 ;
 D ENS^%ZISS
 S IOTM=LRHYTM,IOBM=LRHYBM
 W @IOSTBM
 Q
SCROLOFF() ;
 ;RETURNS SCREEN TO NORMAL SETTINGS Y=0 to 24
 D ENS^%ZISS
 S IOTM=0,IOBM=24
 W @IOSTBM
 D KILL^%ZISS
 Q
